    // Habilita ou desabilita campos e botões
    private void desabilitaHabilitaCampos(boolean cond) {
        jtNome.setEnabled(cond);
        jtEndereco.setEnabled(cond);
        jtBairro.setEnabled(cond);
        jtCidade.setEnabled(cond);
        jcbEstado.setEnabled(cond);
        jftCep.setEnabled(cond);
        jftTelefone.setEnabled(cond);
        jbSalvar.setEnabled(cond);
    }

    private void limparCampos() {
        jtNome.setText("");
        jtEndereco.setText("");
        jtBairro.setText("");
        jtCidade.setText("");
        jftCep.setText("");
        jftTelefone.setText("");
    }

    private void carregarCliente() {
        listaModelClientes = ccliente.getListaClienteController();
        DefaultTableModel modelo = (DefaultTableModel) jtTabelaCliente.getModel();
        modelo.setNumRows(0);
        int cont = listaModelClientes.size();

        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelClientes.get(i).getIdCliente(),
                listaModelClientes.get(i).getCliNome(),
                listaModelClientes.get(i).getCliCidade(),
                listaModelClientes.get(i).getCliTelefone()
            });
        }
    }

