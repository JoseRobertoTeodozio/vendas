    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // Salvar um ciente no banco:
        try {
            mcliente.setIdCliente(Integer.parseInt(this.jtCodigo.getText()));
        } catch (Exception e) {
        }
        mcliente.setCliNome(this.jtNome.getText());
        mcliente.setCliEndereco(this.jtEndereco.getText());
        mcliente.setCliBairro(this.jtBairro.getText());
        mcliente.setCliCidade(this.jtCidade.getText());
        mcliente.setCliUF(this.jcbEstado.getSelectedItem().toString());
        mcliente.setCliCep(this.jftCep.getText());
        mcliente.setCliTelefone(this.jftTelefone.getText());

        if (salvarAlterar.equals("salvar")) {
            if (ccliente.salvarClienteController(mcliente) > 0) {
                JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!", "ATENÇÂO ", JOptionPane.WARNING_MESSAGE);
                carregarCliente();
                this.limparCampos();
                this.desabilitaHabilitaCampos(false);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar cliente!", " ERRO ", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            if (ccliente.atualizarClienteController(mcliente)) {
                JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso!", "ATENÇÂO ", JOptionPane.WARNING_MESSAGE);
                carregarCliente();
                this.limparCampos();
                this.desabilitaHabilitaCampos(false);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar cliente!", " ERRO ", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jbSalvarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // Ecluir um cliente do banco:
        int linha = jtTabelaCliente.getSelectedRow();
        try {
            int codigoCliente = (int) jtTabelaCliente.getValueAt(linha, 0);

            if (ccliente.excluirClienteController(codigoCliente)) {
                JOptionPane.showMessageDialog(this, "Cliente excluido com sucesso!", " ATENÇÂO ", JOptionPane.WARNING_MESSAGE);
                this.carregarCliente();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir cliente!", " ERRO ", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenum regisro selecionado.", " ERRO ", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // Alterar um cliente no banco:
        int linha = jtTabelaCliente.getSelectedRow();
        salvarAlterar = "alterar";
        try {
            int codigoCliente = (int) jtTabelaCliente.getValueAt(linha, 0);

            mcliente = ccliente.getClienteController(codigoCliente);
            jtCodigo.setText(String.valueOf(mcliente.getIdCliente()));
            jtNome.setText(mcliente.getCliNome());
            jtEndereco.setText(mcliente.getCliEndereco());
            jtBairro.setText(mcliente.getCliBairro());
            jtCidade.setText(mcliente.getCliCidade());
            jcbEstado.setSelectedItem(mcliente.getCliUF());
            jftCep.setText(mcliente.getCliCep());
            jftTelefone.setText(mcliente.getCliTelefone());
            this.desabilitaHabilitaCampos(true);
        } catch (Exception e) {
            desabilitaHabilitaCampos(false);
            JOptionPane.showMessageDialog(this, "Código inválido ou nenum regisro selecionado.", " ERRO ", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

